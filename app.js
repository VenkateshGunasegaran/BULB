function change (){
    var img = document.getElementById("bulb");
    if (img.src.match("pngwing")){
        img.src="https://www.freeiconspng.com/thumbs/lightbulb-png/light-bulb-png-bulb-png1247-12.png";
    }
    else {
        img.src="https://w7.pngwing.com/pngs/670/121/png-transparent-incandescent-light-bulb-electric-light-bulb-angle-hand-lamp.png";
    }
}